export type TranslationKey = keyof typeof translations.zh;

type TranslationVars = Record<string, string | number>;

export function normalizeLanguage(language: unknown): Language {
	return language === "en" || language === "zh"
		? language
		: DEFAULT_LANGUAGE;
}

export function translate(
	language: Language,
	key: TranslationKey,
	vars: TranslationVars = {},
): string {
	const normalized = normalizeLanguage(language);
	let value: string = translations[normalized][key] ?? translations.zh[key];
	for (const [name, replacement] of Object.entries(vars)) {
		value = value.split(`{${name}}`).join(String(replacement));
	}
	return value;
}

export function createTranslator(language: Language) {
	return (key: TranslationKey, vars?: TranslationVars) =>
		translate(language, key, vars);
}

export function getDefaultPracticeMessages(language: Language): {
	perfect: string[];
	error: string[];
} {
	const defaults = DEFAULT_PRACTICE_MESSAGES[normalizeLanguage(language)];
	return {
		perfect: [...defaults.perfect],
		error: [...defaults.error],
	};
}

export function formatCompactDuration(
	language: Language,
	seconds: number,
): string {
	const t = createTranslator(language);
	if (seconds < 60) return t("time.seconds", { count: seconds });
	const minutes = Math.floor(seconds / 60);
	const remainingSeconds = seconds % 60;
	if (minutes < 60) {
		return remainingSeconds > 0
			? t("time.minutesSeconds", {
					minutes,
					seconds: remainingSeconds,
				})
			: t("time.minutes", { count: minutes });
	}
	const hours = Math.floor(minutes / 60);
	const remainingMinutes = minutes % 60;
	return remainingMinutes > 0
		? t("time.hoursMinutes", {
				hours,
				minutes: remainingMinutes,
			})
		: t("time.hours", { count: hours });
}

export function formatReviewInterval(
	language: Language,
	unit: "minute" | "hour" | "day",
	count: number,
): string {
	const t = createTranslator(language);
	switch (unit) {
		case "minute":
			return t("time.minute", { count });
		case "hour":
			return t("time.hour", { count });
		case "day":
			return t("time.day", { count });
	}
}

